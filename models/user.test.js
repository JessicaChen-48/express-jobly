/************************************** apply for job */

describe("apply for job", function () {
  test("works", async function () {
    let job = await db.query(`SELECT id FROM jobs`);
    let jobId = job.rows[0].id;

    let application = await User.applyForJob("u2", jobId);

    expect(application).toEqual({ username: "u2", jobId, currentState: "applied" });
  });

  test("does not allow duplicate applications", async function () {
    let job = await db.query(`SELECT id FROM jobs`);
    let jobId = job.rows[0].id;

    try {
      await User.applyForJob("u1", jobId);
      await User.applyForJob("u1", jobId);
      fail();
    } catch (error) {
      expect(error instanceof BadRequestError).toBeTruthy();
    }
  });

  test("not found error if username not found", async function () {
    let job = await db.query(`SELECT id FROM jobs`);
    let jobId = job.rows[0].id;

    try {
      await User.applyForJob("u123", jobId);
    } catch (error) {
      expect(error instanceof NotFoundError).toBeTruthy();
    }
  });

  test("not found error if job id not found", async function () {
    try {
      await User.applyForJob("u1", 0);
    } catch (error) {
      expect(error instanceof NotFoundError).toBeTruthy();
    }
  });
});
/************************************** update application */

describe("update job application state", function () {
  test("works", async function () {
    let appRes = await db.query(`SELECT username, job_id AS "jobId" FROM applications`);
    let {username, jobId} = appRes.rows[0];

    let updatedApp = await User.updateJobApplication(username, jobId, 'accepted');

    expect(updatedApp).toEqual({
      username,
      jobId,
      currentState: 'accepted'
    });
  }); 


  test("errors when invalid enum value", async function () {
    try {
      let appRes = await db.query(`SELECT username, job_id AS "jobId" FROM applications`);
      let {username, jobId} = appRes.rows[0];
      await User.updateJobApplication(username, jobId, 'nope');
    } catch (error) {
      expect(error.message).toContain("invalid input value for enum state:")
    }
  });

});
/************************************** update */

describe("update user", function () {
  const updateData = {
    firstName: "NewF",
    lastName: "NewF",
    email: "new@email.com",
    isAdmin: true,
  };

  test("update user works", async function () {
    let job = await User.update("u1", updateData);
    expect(job).toEqual({
      username: "u1",
      ...updateData,
    });
  });

  test("works: set password", async function () {
    let job = await User.update("u1", {
      password: "new",
    });
    expect(job).toEqual({
      username: "u1",
      firstName: "U1F",
      lastName: "U1L",
      email: "u1@email.com",
      isAdmin: false,
    });
    const found = await db.query("SELECT * FROM users WHERE username = 'u1'");
    expect(found.rows.length).toEqual(1);
    expect(found.rows[0].password.startsWith("$2b$")).toEqual(true);
  });

  test("not found if no such user", async function () {
    try {
      await User.update("nope", {
        firstName: "test",
      });
      fail();
    } catch (err) {
      expect(err instanceof NotFoundError).toBeTruthy();
    }
  });

  test("bad request if no data", async function () {
    expect.assertions(1);
    try {
      await User.update("c1", {});
      fail();
    } catch (err) {
      expect(err instanceof BadRequestError).toBeTruthy();
    }
  });
});

